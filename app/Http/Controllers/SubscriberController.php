    {
        $data = request()->validate([
            'firstName' => 'required',
            'lastName' => 'required',
            'email' => 'required|email',
        ]);

        $user = Subscriber::where('email', '=', $request->email)->first();
            if ($user === null) {

                Subscriber::create($data);

                return response()->json("Subscription successful", 201);
        }
        else{
            return response()->json("Email already exists", 500);
        }

        
 
